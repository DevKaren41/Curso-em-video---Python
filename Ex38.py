num1 = int(input('Escreva o primeiro numero inteiro: '))
num2 = int(input('Escreva o segundo numero inteiro: '))
if num1 < num2:
    print('o primeiro numero é menor que o segundo numero.')
elif num1 > num2:
    print('O primeiro numero é maior que o segundo número.')

elif num1 == num2:
    print('Não existe valor maior, os numeros sao iguais.')